import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
import joblib
import warnings
warnings.filterwarnings("ignore")

from sklearn.ensemble import RandomForestRegressor
from sklearn.model_selection import train_test_split, RandomizedSearchCV, cross_val_score
from sklearn.preprocessing import StandardScaler
from sklearn.metrics import mean_absolute_error, mean_squared_error, r2_score

# ── Constants ────────────────────────────────────────────────────────────────
DATASET_PATH = "cnc_rul_dataset.csv"
MODEL_PATH   = "cnc_rul_model.pkl"
SCALER_PATH  = "cnc_rul_scaler.pkl"
FEATURES     = ["Time_Hours", "Temperature_C", "Vibration_g", "Current_A", "RPM"]
TARGET       = "RUL_Hours"
RANDOM_STATE = 42
TEST_SIZE    = 0.20
CV_FOLDS     = 5


# ── 1. Load Dataset ──────────────────────────────────────────────────────────
def load_data(path):
    df = pd.read_csv(path)
    print(f"[INFO] Dataset loaded | Shape: {df.shape}")
    print(df.head())
    return df


# ── 2. Preprocess ────────────────────────────────────────────────────────────
def preprocess(df):
    # Drop duplicates
    before = len(df)
    df = df.drop_duplicates().reset_index(drop=True)
    print(f"[INFO] Duplicates removed: {before - len(df)}")

    # Fill missing values with median
    missing = df.isnull().sum().sum()
    if missing:
        df.fillna(df.median(numeric_only=True), inplace=True)
        print(f"[INFO] Missing values filled: {missing}")
    else:
        print("[INFO] No missing values found.")

    # Remove outliers using IQR on features + target
    cols = FEATURES + [TARGET]
    n_before = len(df)
    mask = pd.Series([True] * len(df))
    for col in cols:
        q1, q3 = df[col].quantile(0.25), df[col].quantile(0.75)
        iqr = q3 - q1
        mask &= df[col].between(q1 - 1.5 * iqr, q3 + 1.5 * iqr)
    df = df[mask].reset_index(drop=True)
    print(f"[INFO] Outliers removed: {n_before - len(df)} | Remaining: {len(df)}")

    return df


# ── 3. Split & Scale ─────────────────────────────────────────────────────────
def prepare_data(df):
    X = df[FEATURES].values
    y = df[TARGET].values

    X_train, X_test, y_train, y_test = train_test_split(
        X, y, test_size=TEST_SIZE, random_state=RANDOM_STATE
    )

    scaler = StandardScaler()
    X_train = scaler.fit_transform(X_train)   # fit only on train
    X_test  = scaler.transform(X_test)

    print(f"[INFO] Train: {len(X_train)} | Test: {len(X_test)}")
    return X_train, X_test, y_train, y_test, scaler


# ── 4. Tune & Train ──────────────────────────────────────────────────────────
def tune_and_train(X_train, y_train):
    print("[INFO] Starting hyperparameter tuning ...")

    param_dist = {
        "n_estimators"     : [100, 200, 300, 400, 500],
        "max_depth"        : [None, 10, 20, 30, 50],
        "min_samples_split": [2, 5, 10],
        "min_samples_leaf" : [1, 2, 4],
        "max_features"     : ["sqrt", "log2", None],
        "bootstrap"        : [True, False],
    }

    rf = RandomForestRegressor(random_state=RANDOM_STATE, n_jobs=-1)
    search = RandomizedSearchCV(
        rf, param_dist, n_iter=50, cv=CV_FOLDS,
        scoring="neg_mean_absolute_error",
        n_jobs=-1, verbose=1, random_state=RANDOM_STATE, refit=True
    )
    search.fit(X_train, y_train)

    print(f"[INFO] Best params: {search.best_params_}")
    print(f"[INFO] Best CV MAE: {-search.best_score_:.4f}")
    return search.best_estimator_


# ── 5. Cross-Validation ──────────────────────────────────────────────────────
def cross_validate(model, X_train, y_train):
    scores = cross_val_score(model, X_train, y_train, cv=CV_FOLDS, scoring="r2", n_jobs=-1)
    print(f"[INFO] CV R² scores: {np.round(scores, 4)} | Mean: {scores.mean():.4f} | Std: {scores.std():.4f}")


# ── 6. Evaluate ──────────────────────────────────────────────────────────────
def evaluate(model, X_test, y_test):
    y_pred = model.predict(X_test)

    mae  = mean_absolute_error(y_test, y_pred)
    mse  = mean_squared_error(y_test, y_pred)
    rmse = np.sqrt(mse)
    r2   = r2_score(y_test, y_pred)
    mape = np.mean(np.abs((y_test - y_pred) / np.maximum(np.abs(y_test), 1e-10))) * 100

    print("\n" + "=" * 45)
    print("         MODEL EVALUATION RESULTS")
    print("=" * 45)
    print(f"  MAE   : {mae:.4f}")
    print(f"  MSE   : {mse:.4f}")
    print(f"  RMSE  : {rmse:.4f}")
    print(f"  R²    : {r2:.4f}")
    print(f"  MAPE  : {mape:.4f} %")
    print("=" * 45 + "\n")

    return y_pred


# ── 7. Feature Importance Plot ───────────────────────────────────────────────
def plot_feature_importance(model):
    importances = model.feature_importances_
    sorted_idx  = np.argsort(importances)
    sorted_feats = [FEATURES[i] for i in sorted_idx]
    sorted_imps  = importances[sorted_idx]

    plt.figure(figsize=(8, 4))
    bars = plt.barh(sorted_feats, sorted_imps, color="#1f77b4", edgecolor="white")
    for bar, val in zip(bars, sorted_imps):
        plt.text(val + 0.002, bar.get_y() + bar.get_height() / 2,
                 f"{val:.4f}", va="center", fontsize=9)
    plt.xlabel("Importance (Gini)")
    plt.title("Feature Importance – Random Forest", fontweight="bold")
    plt.tight_layout()
    plt.savefig("feature_importance.png", dpi=150)
    plt.show()
    print("[INFO] Feature importance plot saved.")


# ── 8. Actual vs Predicted Plot ──────────────────────────────────────────────
def plot_actual_vs_predicted(y_test, y_pred):
    residuals = y_test - y_pred

    fig, axes = plt.subplots(1, 2, figsize=(13, 5))

    # Scatter plot
    axes[0].scatter(y_test, y_pred, alpha=0.6, color="#1f77b4", edgecolors="white", s=40)
    lims = [min(y_test.min(), y_pred.min()), max(y_test.max(), y_pred.max())]
    axes[0].plot(lims, lims, "r--", linewidth=1.5, label="Perfect prediction")
    axes[0].set_xlabel("Actual RUL (Hours)")
    axes[0].set_ylabel("Predicted RUL (Hours)")
    axes[0].set_title("Actual vs Predicted RUL", fontweight="bold")
    axes[0].legend()
    axes[0].grid(alpha=0.3)

    # Residuals histogram
    axes[1].hist(residuals, bins=20, color="#2ca02c", edgecolor="white", alpha=0.8)
    axes[1].axvline(0, color="red", linestyle="--", linewidth=1.5, label="Zero error")
    axes[1].set_xlabel("Residual (Actual − Predicted)")
    axes[1].set_ylabel("Frequency")
    axes[1].set_title("Residuals Distribution", fontweight="bold")
    axes[1].legend()
    axes[1].grid(alpha=0.3)

    plt.tight_layout()
    plt.savefig("actual_vs_predicted.png", dpi=150)
    plt.show()
    print("[INFO] Actual vs Predicted plot saved.")


# ── 9. Save & Load Model ─────────────────────────────────────────────────────
def save_model(model, scaler):
    joblib.dump(model,  MODEL_PATH,  compress=3)
    joblib.dump(scaler, SCALER_PATH, compress=3)
    print(f"[INFO] Model saved  → {MODEL_PATH}")
    print(f"[INFO] Scaler saved → {SCALER_PATH}")


def load_model():
    model  = joblib.load(MODEL_PATH)
    scaler = joblib.load(SCALER_PATH)
    print("[INFO] Model and scaler loaded from disk.")
    return model, scaler


# ── 10. predict_rul() ────────────────────────────────────────────────────────
def predict_rul(time_hours, temperature_c, vibration_g, current_a, rpm,
                model=None, scaler=None):
    """
    Predict RUL given sensor readings.

    Parameters
    ----------
    time_hours    : float – machine operating time (hours)
    temperature_c : float – temperature in Celsius
    vibration_g   : float – vibration in g
    current_a     : float – current in Amperes
    rpm           : float – spindle speed (RPM)

    Returns
    -------
    float – Predicted RUL in hours
    """
    if model is None or scaler is None:
        model, scaler = load_model()

    X = np.array([[time_hours, temperature_c, vibration_g, current_a, rpm]], dtype=np.float64)
    X_scaled = scaler.transform(X)
    rul = float(model.predict(X_scaled)[0])
    return max(0.0, rul)   # RUL cannot be negative


# ── Health Status ────────────────────────────────────────────────────────────
def health_status(rul):
    if rul <= 500:
        return "CRITICAL  – Immediate maintenance required!"
    elif rul <= 2000:
        return "WARNING   – Schedule maintenance soon."
    elif rul <= 5000:
        return "MODERATE  – Monitor closely."
    else:
        return "GOOD      – Machine operating normally."


# ── Main Pipeline ────────────────────────────────────────────────────────────
def main():
    print("=" * 50)
    print("   CNC Machine RUL Prediction – ML Pipeline")
    print("=" * 50)

    df             = load_data(DATASET_PATH)
    df_clean       = preprocess(df)
    X_train, X_test, y_train, y_test, scaler = prepare_data(df_clean)
    model          = tune_and_train(X_train, y_train)
    cross_validate(model, X_train, y_train)
    y_pred         = evaluate(model, X_test, y_test)
    plot_feature_importance(model)
    plot_actual_vs_predicted(y_test, y_pred)
    save_model(model, scaler)

    # ── Demo predictions ─────────────────────────────────────────────────
    loaded_model, loaded_scaler = load_model()

    demo = [
        (2000,  42.68, 0.776, 3.17, 1492),   # early stage
        (6000,  58.84, 1.988, 4.59, 1476),   # mid stage
        (9500,  72.98, 3.048, 5.82, 1462),   # near end of life
    ]

    print("\n" + "=" * 72)
    print("  SAMPLE PREDICTIONS")
    print("=" * 72)
    print(f"{'Time':>6} {'Temp':>7} {'Vib':>6} {'Curr':>6} {'RPM':>5} │ {'Pred RUL':>10}  Status")
    print("-" * 72)
    for args in demo:
        rul = predict_rul(*args, model=loaded_model, scaler=loaded_scaler)
        print(f"{args[0]:>6} {args[1]:>7.2f} {args[2]:>6.3f} {args[3]:>6.2f} {args[4]:>5} │"
              f" {rul:>10.1f}  {health_status(rul)}")
    print("=" * 72)


if __name__ == "__main__":
    main()